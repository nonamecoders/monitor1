package com.alan.monitor1.service;

import com.alan.monitor1.mapper1.SmsMapper;
import org.springframework.stereotype.Service;

import javax.annotation.Resource;
import java.util.HashMap;

@Service("com.alan.monitor1.service.SmsService")
public class SmsService {
    @Resource(name = "com.alan.monitor1.mapper1.SmsMapper")
    SmsMapper smsMapper;


    public int selectSmsService() throws Exception{

        return smsMapper.selectSms();
    }

    public int insertSmsService(String msg) throws  Exception{


        return smsMapper.insertSms(msg);
    }
}
